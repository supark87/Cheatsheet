#VIM commands I look up a lot.. 

	J		# capital J, moves line up to current cursor postition 
	
	:2,5 d  	# delete lines 2 through 5

	:5,17s/^/#	# comment out lines 5-17, start of line (^) with #

	:%s/old/new/g	# replace all old with new throughout the file (g) 
	:%s/old/new/gc	# replace all old with new throughout the file (g) with (c)onfirmation 

	shift + }	# jump to next paragraph 
	shift + {	# jump to previous paragraph
	
